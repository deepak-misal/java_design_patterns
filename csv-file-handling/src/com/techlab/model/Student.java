package com.techlab.model;

public class Student {
	private int rollNo;
	private String name;
	private String location;
	private float cgpa;
	private float greScore;
	
	public Student(int rollNo,String name,String location,float cgpa,float greScore) {
		this.rollNo=rollNo;
		this.name=name;
		this.location=location;
		this.cgpa=cgpa;
		this.greScore=greScore;
	}
	
}

